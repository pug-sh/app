const resolveKey = (countryIso: string, region: string) => {
  for (const key of regionLookupKeys(countryIso, region)) {
    const alias = REGION_ALIASES[key] ?? key
    if (REGION_CENTROIDS[alias]) return alias
  }
  return null
}

export const resolveRegionCentroid = (countryIso: string, region: string | undefined) => {
  if (!region?.trim()) return null
  const key = resolveKey(countryIso, region)
  return key ? REGION_CENTROIDS[key] : null
}

const regionLookupKeys = (countryIso: string, region: string) => {
  const country = countryIso.toUpperCase()
  const token = region.trim().toUpperCase().normalize('NFD').replace(/\p{M}/gu, '')
  const keys = [`${country}:${token}`]
  if (token.includes('-')) {
    const parts = token.split('-')
    if (parts[0] === country && parts[1]) keys.push(`${country}:${parts[1]}`)
    if (parts[0] === 'US' && parts[1]) keys.push(`US:${parts[1]}`)
  }
  return keys
}
